<?php

namespace common\onecms;

use common\models\Review;
use common\models\ReviewCategory;
use yii\base\Widget;

class RelatedReviewWidget extends Widget
{
    public $category_id;
    public $node_id;
    public function init()
    {
        parent::init(); // TODO: Change the autogenerated stub
    }

    public function run()
    {
        parent::run(); // TODO: Change the autogenerated stub
        $categoriesData = ReviewCategory::findOne($this->category_id)->leaves()->all();
        $categories = array();
        $categories[] = $this->category_id;
        if($categoriesData) {
            foreach($categoriesData as $category) {
                $categories[] = $category->id;
            }
        }
        $offset = Review::find()->where(['in', 'category_id', $categories])
            ->andWhere(['>', 'id', $this->node_id])
            ->andFilterWhere(['status' => 10])
            ->orderBy(['id' => SORT_DESC])->count();
        $nodes = Review::find()->where(['in', 'category_id', $categories])
            ->andFilterWhere(['status' => 10])->orderBy(['id' => SORT_DESC])->limit(4)->offset($offset)->all();
        return $this->render('RelatedReviewWidget', [
            'nodes' => $nodes,
        ]);
    }
}